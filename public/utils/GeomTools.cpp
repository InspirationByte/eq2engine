#include "GeomTools.h"

Vector3D UTIL_VertexAtPos(const ubyte* vertexBaseOffset, int stride, int vertexIndex)
{
	int vertexStrideOffset = vertexIndex*stride;
	ubyte* pDataVertex = (ubyte*)vertexBaseOffset + vertexStrideOffset;

	return *(Vector3D*)pDataVertex;
}